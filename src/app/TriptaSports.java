    private void preparePanelA() {
        if (gameOnBoardA) {
            
            jTabbedPaneA.setSelectedComponent(runningA);
            lblRunningBoardAStartTime.setText(runningGameOnBoardA.getStartTime().toString());
            lblRunningBoardAMatchTime.setText(time);
            lblRunningBoardAPlayerA.setText(runningGameOnBoardA.getPlayerOne());
            lblRunningBoardAPlayerB.setText(runningGameOnBoardA.getPlayerTwo());
            lblRunningBoardAGameType.setText(runningGameOnBoardA.getGameType());
            lblRunningBoardARemark.setText(runningGameOnBoardA.getRemark());

            Match m = new MatchRunningConfirmOptionPane1("BOARD-A").showEnd();
            if (m != null) {

                m.setId(runningGameOnBoardA.getId());
                runningGameOnBoardA.setEndTime(m.getEndTime());
                runningGameOnBoardA.setRemark("Completed!");
                if (new MatchDao().endMatch(m)) {
                    // update game end
                new GameDao().endGameDetailsUpdate(prepareEndGame(runningGameOnBoardA));
                // if fails to update end Game on game_table for completed match
                // create ui module to revisit from app and validate game_end
                
                    System.out.println("Match Ends");
                    gameOnBoardA = false; // game started on board-A
                    runningGameOnBoardA = null;
                    jTabbedPaneA.setSelectedComponent(startA);
                } else {
                    System.out.println("Failed to end match! DB");

                }
            } else {
                System.out.println("Game end Cancled!");
            }

        } else {
            jTabbedPaneA.setSelectedComponent(startA);
            
        }
    }
    
    
