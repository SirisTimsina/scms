    private void btnStartGameOnAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStartGameOnAActionPerformed
        // TODO add your handling code here:
        if(!gameOnBoardA){
        Match match = new Match();
        match.setStartTime(LocalTime.now());
        match.setDate(LocalDate.now());
        match.setBoard("BOARD-A");
        match.setEndTime(LocalTime.parse("00:00:00"));
        match.setGameType(startAGameType.getSelectedItem().toString());
        if(!"NULL".equals(startAPlayerA.getSelectedItem())){
        match.setPlayerOne(startAPlayerA.getSelectedItem().toString().concat("A"));
        currentPlayers.get("AA").setName(match.getPlayerOne());
        }else{
            currentPlayers.get("AA").setName("PlayerAA");
            match.setPlayerOne("PlayerAA");
        }
        if(!"NULL".equals(startAPlayerB.getSelectedItem())){
        match.setPlayerTwo(startAPlayerB.getSelectedItem().toString().concat("B"));
        currentPlayers.get("AB").setName(match.getPlayerTwo());
        }else{
            currentPlayers.get("AB").setName("PlayerAB");
            match.setPlayerTwo("PlayerAB");
        }
        if(null!=startARemark.getText()){
        match.setRemark(startARemark.getText());
        }
        match.setRate(Integer.parseInt(startARate.getSelectedItem().toString()));
        
        runningGameOnBoardA = new MatchDao().setMatch(match);
        if(runningGameOnBoardA!=null){
            gameOnBoardA=true;
            jTabbedPaneA.setSelectedComponent(runningA);
            // add entry into game_table now
            if(new GameDao().saveGame(prepareStartGame(runningGameOnBoardA))){
               
                
                lblPlayersA.setText("Players: "+currentPlayers.get("AA").getName()+"< VS >"+ currentPlayers.get("AB").getName() );
                mainPanel.repaint();
                mainPanel.revalidate();
//                mainPanel.repaint();
                    System.out.println("PanelA: All set for A..");
                    
                    
                }
            
        }
        }else{
            preparePanelA();
            mainPanel.repaint();
            mainPanel.revalidate();
            System.out.println("Game already running on A!");
        }  
        
    }//GEN-LAST:event_btnStartGameOnAActionPerformed

