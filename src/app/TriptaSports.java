    public Game prepareStartGame(Match m){
        Game game = new Game();
                game.setMatchId(m.getId());
                game.setPaid("0");
                game.setMatchTime(0);
                game.setPaidAt(LocalTime.MIDNIGHT);
                game.setPaidDate(LocalDate.parse("2000-01-01"));
                game.setRemark("Running");
                return game;
    }
    
    public Game prepareEndGame(Match m){
        Game game = new Game();
        game.setMatchTime(calculateMatchTime(m));
        game.setAmount(game.getMatchTime()*m.getRate());
        game.setMatchId(m.getId());
        game.setRemark(m.getRemark());
        // payer, paid_at, paid_date, discount and remark is updated during payment
        return game;
    }
    
    
    public static int calculateMatchTime(Match m){
        int matchTime = 0;
        LocalTime startTime = m.getStartTime();
        LocalTime endTime = m.getEndTime();
        if(startTime.isBefore(endTime)){
            int shour = startTime.getHour();
            int sminute = startTime.getMinute();
            
            int ehour = endTime.getHour();
            int eminute = endTime.getMinute();
            // st: 12:33 et: 12:55 => matchtime = (12-12)*60 + (55-33) = 22
            // st: 12:30 et: 13:10 => matchtime = (12-13)*60 + (10-30) = 60 - 20 = 40
            matchTime = (ehour - shour)* 60 + (eminute - sminute);
        }else{
            System.out.println("Time calculation error..");
        }
        
        return matchTime;
    }

