    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // start match on B
        
          Match m = new MatchStartOptionPane().showStart();
        if (m != null) {
            m.setBoard("BOARD-B");
            m.setPlayerOne(m.getPlayerOne().concat("A"));
            m.setPlayerTwo(m.getPlayerTwo().concat("B"));
            m.setDate(LocalDate.now());
            m.setStartTime(LocalTime.now());
            
            runningGameOnBoardB = new MatchDao().setMatch(m);
            // save match start detail into database
            // upon success do following
            if (runningGameOnBoardB != null) {
                gameOnBoardB = true;  // game started on board-A
                currentPlayers.get("BA").setName(runningGameOnBoardB.getPlayerOne());
                currentPlayers.get("BB").setName(runningGameOnBoardB.getPlayerTwo());
                // set players for display
                lblCurrentTime.setBackground(Color.GREEN);
                bStart.setText(time);
                jButton6.setEnabled(false);
                jButton5.setEnabled(true);
                matchIdA.setText("Match ID: B" + runningGameOnBoardB.getId());
                System.out.println("Match started!");
                
                // add other stats of game
                if(new GameDao().saveGame(prepareStartGame(runningGameOnBoardB))){
                    
                lblPlayersB.setText("Players: "+currentPlayers.get("BA").getName()+"< VS >"+ currentPlayers.get("BB").getName() );
                    System.out.println(runningGameOnBoardB .getPlayerOne());
                    System.out.println("All set for B..");
                }
                
            } else {
                System.out.println("Database problem!");
            }
            System.out.println(m.toString());
        } else {
            System.out.println("Failed to start a match!");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

