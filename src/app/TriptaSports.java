    private void buttonBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonBActionPerformed
        mainPanel.removeAll();
        mainPanel.add(panelB);
        
        currentBoard = "BOARD-B";
        if (gameOnBoardB) {

            jTabbedPaneB.setSelectedComponent(runningB);
            lblRunningBoardBStartTime.setText(runningGameOnBoardB.getStartTime().toString());
            lblRunningBoardBMatchTime.setText(time);
            lblRunningBoardBPlayerA.setText(runningGameOnBoardB.getPlayerOne());
            lblRunningBoardBPlayerB.setText(runningGameOnBoardB.getPlayerTwo());
            lblRunningBoardBGameType.setText(runningGameOnBoardB.getGameType());
            lblRunningBoardBRemark.setText(runningGameOnBoardB.getRemark());

            Match m = new MatchRunningConfirmOptionPane1("BOARD-B").showEnd();
            if (m != null) {

                m.setId(runningGameOnBoardB.getId());
                runningGameOnBoardB.setEndTime(m.getEndTime());
                runningGameOnBoardB.setRemark("Completed!");
                if (new MatchDao().endMatch(m)) {
                    // update game end
                new GameDao().endGameDetailsUpdate(prepareEndGame(runningGameOnBoardB));
                // if fails to update end Game on game_table for completed match
                // create ui module to revisit from app and validate game_end
                   System.out.println("Match Ends");
                    gameOnBoardB = false; // game started on board-A
                    runningGameOnBoardB = null;
                    jTabbedPaneB.setSelectedComponent(startB);
                } else {
                    System.out.println("Failed to end match! DB");

                }
            } else {
                System.out.println("Game end Cancled!");
            }

        } else {
            jTabbedPaneB.setSelectedComponent(startB);
        }
        mainPanel.repaint();
        mainPanel.revalidate();
    }//GEN-LAST:event_buttonBActionPerformed

